# Function for addition 
def addition(x,y):
    result = x + y
    return result

# Function for substraction 
def substraction(x,y):
    result = x - y
    return result

# Function for multiplication 
def mul(x,y):
    result = x*y
    return result

#Function for devivsion 
def dev (x,y):
    result = x/y
    return result
